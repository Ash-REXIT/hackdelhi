export function getVerificationReport(timesheetId: string) {
  return verificationReports.find((r) => r.timesheetId === timesheetId)
}

export function getTimesheet(id: string) {
  return timesheets.find((t) => t.id === id)
}

export function getInvoice(id: string) {
  return invoices.find((i) => i.id === id || i.invoiceNumber === id)
}

export function getClientInvoices(clientId: string) {
  return invoices.filter((i) => i.clientId === clientId)
}

export function getClientTimesheets(clientId: string) {
  return timesheets.filter((t) => t.clientId === clientId)
}
